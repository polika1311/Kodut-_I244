<h1>Jupiter</h1>

<br/>
<p><b>Jupiter </b> on Päikesest kauguselt viies planeet ja Päikesesüsteemi kõige suurem planeet. Hiidplaneet Jupiter ületab kõigi teiste planeetide kogumassi umbes kaks ja pool korda. Päikese massist on Jupiteri mass ligikaudu 1000 korda väiksem. Astronoomid teavad Jupiteri juba antiikajast.[1] Roomlased panid sellele nime oma jumala Jupiteri järgi. Maa pealt vaadatuna võib Jupiteri tähesuurus olla kuni ?2,94, mis on piisavalt hele, et jätta varje.[2] Kuu ja Veenuse järel on Jupiter heleduselt kolmas taevakeha. (Opositsiooni ajal võib Marss paista lühikest aega sama hele kui Jupiter.)
</p>
<p><em> Jupiter </em> koosneb peamiselt heeliumist, mis moodustab Jupiteri massist ühe neljandiku, kõigist molekulidest moodustavad heeliumi molekulid aga vaid kümnendiku. Jupiteril võib olla raskematest elementidest tuum,[3] kuid nagu kõigil hiidplaneetidel, puudub ka Jupiteril tahke pind. Kiire pöörlemise tõttu on Jupiter lapik sferoid. Jupiteri välimine atmosfäär on silmanähtavalt jaotunud vastavalt laiuskraadidele erinevateks kihtideks, mille piirialadel esineb turbulentsi ja torme. Üks tähelepänuväärsemaid Jupiteri atmosfäärinähtusi on Suur Punane Laik, mis on olnud olemas juba vähemalt 17. sajandist, mil seda vaadeldi esmakordselt teleskoobiga. Jupiteri ümbritseb tugev magnetväli ja palja silmaga nähtamatu rõngaste süsteem. Jupiteril on vähemalt 67 kaaslast, sealhulgas neli suurt kaaslast, mille avastas 1610. aastal Galileo Galilei. Neist kõige suurema, Ganymedese läbimõõt on suurem kui Merkuuril.
</p>
