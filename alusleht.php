<h2>Mass ja suurus</h2> 
<br/>
<p>
Jupiteri mass on 2,5 korda suurem kui kõigi teiste Päikesesüsteemi planeetide kogumass. See tähendab, et Jupiteri 
ja Päikese massikese asub väljaspool Päikest. Jupiteri kõrval on Maa imeväike, kuid märkimisväärselt suurema tihedusega.
 Jupiteri ruumala on 1321 korda suurem kui Maa ruumala, kuid selle mass on vaid 318 korda suurem. Jupiteri raadius on
  umbes kümnendik Päikese raadiusest[11] ja selle mass on umbes 0,001 Päikese massi, seega on Päike ja Jupiter sarnase 
  tihedusega.
